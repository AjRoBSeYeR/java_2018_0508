package com.ipartek.formacion.basicos.estructura.iterativa;

/**
 * Programa Java que muestre los números del 100 al 1 utilizando la instrucción
 * do...while.
 * 
 * @author Curso
 *
 */
public class Ejercicio5 {

	public static void main(String[] args) {

		int i = 100;

		do {
			System.out.print(i + " ");
			i--;
		} while (i >= 0);

	}

}
