package com.ipartek.formacion.basicos.estructura.iterativa;

/**
 * Programa Java que muestre los números del 1 al 100 utilizando la instrucción
 * do...while.
 * 
 * @author Curso
 *
 */
public class Ejercicio2 {

	public static void main(String[] args) {

		int i = 0;

		do {
			System.out.print(i + " ");
			i++;
		} while (i <= 100);

	}

}
