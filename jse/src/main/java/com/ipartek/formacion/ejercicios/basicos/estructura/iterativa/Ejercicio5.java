package com.ipartek.formacion.ejercicios.basicos.estructura.iterativa;

/**
 * 
 * @author Curso1 5. Programa Java que muestre los números del 100 al 1 utilizando la instrucción do..while 
 *
 */
public class Ejercicio5 {
	public static void main(String[] args) {
		 int x = 100;
	        System.out.println("numeros del 100 al 1: ");
	         do{
	                   System.out.println(x);
	                   x--;
	        }while(x>=1);
	}
}
