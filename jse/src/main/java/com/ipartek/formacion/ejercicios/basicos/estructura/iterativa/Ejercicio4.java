package com.ipartek.formacion.ejercicios.basicos.estructura.iterativa;

/**
 * 
 * @author Curso1 4. Programa Java que muestre los números del 100 al 1
 *         utilizando la instrucción while
 *
 */
public class Ejercicio4 {
	public static void main(String[] args) {
		System.out.println("numeros del 100 al 1: ");
        int x=100;
        while(x>=1)
        {
                  System.out.println(x);
                  x--;
        }
	}
}
