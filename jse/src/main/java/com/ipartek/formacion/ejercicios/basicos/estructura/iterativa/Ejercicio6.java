package com.ipartek.formacion.ejercicios.basicos.estructura.iterativa;

/**
 * 
 * @author Curso1 6. Programa Java que muestre los números del 100 al 1
 *         utilizando la instrucción for
 *
 */
public class Ejercicio6 {
	public static void main(String[] args) {
		System.out.println("numeros del 100 al 1: ");
		for (int x = 100; x >= 1; x--)
			System.out.println(x);
	}
}
