CREATE DEFINER=`root`@`localhost` PROCEDURE `prestamoInsert`(
	IN `p_alumno` INT,
	IN `p_libro` INT,
	IN `p_fecha` DATE
    
,
	OUT `o_alumno_ok` BOOLEAN, 
    OUT `o_libro_ok` BOOLEAN,
    OUT `o_fecha_fin` DATE, 
    OUT `o_editorial_id` INT,
    OUT `o_editorial_nombre` VARCHAR(45),
    OUT `o_libro_titulo` VARCHAR(150),
    OUT `o_libro_isbn` VARCHAR(20),
    OUT `o_alumno_nombre` VARCHAR(45)
)
BEGIN

IF ((SELECT COUNT(id_libro) FROM prestamo WHERE id_libro = p_libro AND fecha_retorno IS NULL) = 0)
THEN SET o_libro_ok := TRUE;
ELSE  SET o_libro_ok := FALSE;
END IF;

IF ((SELECT COUNT(id_alumno) FROM prestamo WHERE id_alumno = p_alumno AND fecha_retorno IS NULL) = 0) THEN 
SET o_alumno_ok := TRUE;
ELSE SET o_alumno_ok := FALSE;
END IF;

IF (o_libro_ok = TRUE && o_alumno_ok = TRUE)
THEN INSERT INTO prestamo (id_libro, id_alumno, fecha_inicio) VALUES (p_libro, p_alumno, p_fecha);

SET o_fecha_fin := (SELECT fecha_fin FROM prestamo WHERE id_libro = p_libro AND id_alumno = p_alumno AND fecha_inicio = p_fecha );

SET o_editorial_id := (SELECT id_editorial FROM libro WHERE idlibro = p_libro);
SET o_editorial_nombre := (SELECT nombre FROM editorial WHERE ideditorial = o_editorial_id);

SET o_alumno_nombre := (SELECT nombre FROM alumno WHERE idalumno = p_alumno);

SET o_libro_titulo := (SELECT titulo FROM libro WHERE idlibro = p_libro);
SET o_libro_isbn := (SELECT isbn FROM libro WHERE idlibro = p_libro);

END IF;

END ;;
