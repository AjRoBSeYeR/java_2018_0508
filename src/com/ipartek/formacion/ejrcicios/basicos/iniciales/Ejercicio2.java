package com.ipartek.formacion.ejrcicios.basicos.iniciales;
/**
 * 
 * @author Asier Cornejo
 *  Programa java que realice lo siguiente: declarar dos
 *         variables X e Y de tipo int, dos variables N y M de tipo double y
 *         asigna a cada una un valor. A continuación reliza y muestra muestra
 *         por pantalla una serie de operaciones entre ellas.
 */
public class Ejercicio2 {

	public static void main(String[] args) {
		int x = 5;
		int y = 7;
		double n = 12;
		double m = 56;

		System.out.println("X + Y =" + (x + y));
		System.out.println("N - M =" + (n - m));
		System.out.println("X + N =" + (x + n));
		System.out.println("M + Y =" + (m + y));
	}

}
