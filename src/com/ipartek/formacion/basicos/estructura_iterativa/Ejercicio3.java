package com.ipartek.formacion.basicos.estructura_iterativa;

/*
 * Programa Java que muestre los números del 1 al 100 utilizando la instrucción for.
 */
public class Ejercicio3 {

	public static void main(String[] args) {

		for (int i = 0; i <= 100; i++) {
			System.out.print(i + " ");
		}

	}

}
