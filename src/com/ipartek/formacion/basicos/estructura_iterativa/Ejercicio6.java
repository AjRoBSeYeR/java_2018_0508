package com.ipartek.formacion.basicos.estructura_iterativa;

/*
 * Programa Java que muestre los números del 100 al 1 utilizando la instrucción for.
 */
public class Ejercicio6 {

	public static void main(String[] args) {

		for (int i = 100; i >= 0; i--) {
			System.out.print(i + " ");
		}

	}

}
